package com.dxc.darklight.model;

import java.sql.Timestamp;

import org.nutz.dao.entity.annotation.Column;
import org.nutz.dao.entity.annotation.PK;
import org.nutz.dao.entity.annotation.Table;

/**
 * user_stock
 * 
 */
@Table("user_stock")
@PK( {"id"} )
public class UserStock{
	@Column("id")
	private Integer id;
	@Column("user_id")
	private String userId;
	@Column("stock_code")
	private String stockCode;
	@Column("create_dtm")
	private Timestamp createDtm;
	@Column("update_dtm")
	private Timestamp updateDtm;
	@Column("update_by")
	private String updateBy;
	@Column("create_by")
	private String createBy;
	@Column("version")
	private String version;
	@Column("is_deleted")
	private Integer isDeleted;
	@Column("ex_one")
	private String exOne;
	@Column("ex_two")
	private Integer exTwo;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getStockCode() {
		return stockCode;
	}
	public void setStockCode(String stockCode) {
		this.stockCode = stockCode;
	}
	public Timestamp getCreateDtm() {
		return createDtm;
	}
	public void setCreateDtm(Timestamp createDtm) {
		this.createDtm = createDtm;
	}
	public Timestamp getUpdateDtm() {
		return updateDtm;
	}
	public void setUpdateDtm(Timestamp updateDtm) {
		this.updateDtm = updateDtm;
	}
	public String getUpdateBy() {
		return updateBy;
	}
	public void setUpdateBy(String updateBy) {
		this.updateBy = updateBy;
	}
	public String getCreateBy() {
		return createBy;
	}
	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public Integer getIsDeleted() {
		return isDeleted;
	}
	public void setIsDeleted(Integer isDeleted) {
		this.isDeleted = isDeleted;
	}
	public String getExOne() {
		return exOne;
	}
	public void setExOne(String exOne) {
		this.exOne = exOne;
	}
	public Integer getExTwo() {
		return exTwo;
	}
	public void setExTwo(Integer exTwo) {
		this.exTwo = exTwo;
	}
	
}
